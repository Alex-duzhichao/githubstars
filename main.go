func main() {
	urlSet := map[string]struct{}{}
	for _, url := range repoURLs {
		urlSet[url] = struct{}{}
	}

	ch := make(chan string, len(urlSet))
	for url := range urlSet {
		ch <- url
	}
	close(ch)

	number := 50
	var wg sync.WaitGroup
	wg.Add(number)
	var mu sync.Mutex
	var repositories []*github.Repository
	for i := 0; i < number; i++ {
		go func() {
			defer wg.Done()
			ctx := context.Background()
			ts := oauth2.StaticTokenSource(
				&oauth2.Token{AccessToken: os.Args[1]},
			)
			tc := oauth2.NewClient(ctx, ts)
			client := github.NewClient(tc)

			for url := range ch {
				segments := strings.Split(url, "/")
				if len(segments) != 5 {
					continue
				}

				owner := segments[3]
				repo := segments[4]

				repository, _, err := client.Repositories.Get(context.Background(), owner, repo)
				if err != nil {
					fmt.Println(err)
					continue
				}

				mu.Lock()
				repositories = append(repositories, repository)
				fmt.Printf("repo: %v, star count: %v\n", *repository.HTMLURL, *repository.StargazersCount)
				mu.Unlock()
			}
		}()
	}

	wg.Wait()
	sort.Slice(repositories, func(i, j int) bool {
		return repositories[i].GetStargazersCount() < repositories[j].GetStargazersCount()
	})

	fmt.Printf("------------------------------------\n\n\n\n\n")
	for _, repo := range repositories {
		fmt.Printf("repo: %v, star count: %v\n", *repo.HTMLURL, *repo.StargazersCount)
	}
}
